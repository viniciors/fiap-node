const sum = (num1, num2) => {
    return num1 + num2
}

const minus = (num1, num2) => {
    return num1 - num2
}

const mult = (num1, num2) => {
    return num1 * num2
}

const divide = (num1, num2) => {
    return num1 / num2
}

const square = (num) => {
    return num * num
}

module.exports = {sum, minus, mult, divide}